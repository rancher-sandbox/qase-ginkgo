package qase_test

import (
	"testing"

	. "github.com/onsi/ginkgo/v2"
	. "github.com/onsi/gomega"
	. "github.com/rancher-sandbox/qase-ginkgo/qase"
)

func FailWithReport(message string, callerSkip ...int) {
	// Ensures the correct line numbers are reported
	Fail(message, callerSkip[0]+1)
}

func TestQASE(t *testing.T) {
	RegisterFailHandler(FailWithReport)
	RunSpecs(t, "QASE Test Suite")
}

// To be able to log test results in Qase
var testCaseID int64

var _ = ReportBeforeEach(func(report SpecReport) {
	// Log execution
	GinkgoWriter.Println("ReportBeforeEach executed")

	// Reset case ID
	testCaseID = -1
})

var _ = ReportAfterEach(func(report SpecReport) {
	// Log execution
	GinkgoWriter.Println("ReportAfterEach executed")

	// Add result in Qase if asked
	Qase(testCaseID, report)
})
